function InitQuestion14()
{
    const type = QMAKE_OPENANS;
    const questionIndex = 13;
    var weight = DecodeNumber('azRE+gUOqG4=', 307, 0, 99999);
    var numOfAnswers = 0;
    var question = new Question(type, weight, numOfAnswers);
    question.answers.length = 0;
    question.shortTextQuestion = "List out the important aspects of thesis writing (HINT: I/B. P. SS. SS. LR. M. THF. DA. RF. R. C. R.)";
    var comment = $('#comment' + questionIndex).val();
    var answer = new Answer(0,0,1,0,comment);
    question.answers.push(answer);
    return question;

}






